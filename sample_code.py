def add(a, b):
    """Add two numbers.

    Args:
        a (int): first number
        b (int): second number
        c (int): extra number not actually in signature
    """
    return a + b


def greet(name, age):
    """Return a greeting.

    Args:
        name (str): person's name
    """
    return f"Hello {name}, you are {age}!"